#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	char	*cur;
	size_t	i;

	cur = s;
	i = 0;
	while (i++ < n)
	{
		*cur = c;
		cur++;
	}
	return (s);
}
